% This script defines several simple polyhedra.
% A polyhedron with N faces is represented as a cellular array of size N. 
% Each element of the array represents one face.
% A face with K vertices is represented as a K x 3 array, where each
% columns of the array is contains the coordinates of the vertices.
% The columns are in counter-clockwise order, as viewed by someone
% outside the solid looking at the face.


